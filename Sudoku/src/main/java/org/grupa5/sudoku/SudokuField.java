package org.grupa5.sudoku;



public class SudokuField {

    private int value;

    /**
     * A simple getter.
     * @return Value of member variable 'value'
     */

    public int getFieldValue() {
        return this.value;
    }

    /**
     * A simple setter.
     * @param value - Set the field 'value' to this param
     */

    public void setFieldValue(int value) {
        this.value = value;
    }

}